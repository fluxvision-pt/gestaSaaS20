// Função para agrupar por região
export const getTimezonesByRegion = () => {
  const grouped = timezones.reduce((acc, timezone) => {
    if (!acc[timezone.region]) {
      acc[timezone.region] = [];
    }
    acc[timezone.region].push(timezone);
    return acc;
  }, {});
  
  return grouped;
};

// Função para buscar timezone por valor
export const getTimezoneByValue = (value) => {
  return timezones.find(tz => tz.value === value);
};